public class Primitive_Datatypes 
{
	public static void main(String[] args) 
	{
		char c='M';
		byte b=2;
		short s=42;
		int i=433;
		float f=3.1428714f;
		double d1=2.68902;
		double d2=3.98005d;
		boolean k=true;
		
		System.out.println("char: "+c+"\n"+
						  "byte: "+b+"\n"+
						  "short: "+s+"\n"+
						  "int: "+i+"\n"+
						  "float: "+f+"\n"+
						  "double1: "+d1+"\n"+
						  "double2: "+d2+"\n"+
						  "boolean: "+k+"\n");
	}

}
